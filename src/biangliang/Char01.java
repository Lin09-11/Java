public class Char01 {

    public static void main(String[] args) {
        char c1='a';
        char c2='\t';
        char c3='韩';
        char c4=97;//说明：字符类型可以直接存放一个数字

        System.out.println(c1);//a
        System.out.println(c2);//空一行
        System.out.println(c3);//韩
        System.out.println(c4);//a
    }
}
